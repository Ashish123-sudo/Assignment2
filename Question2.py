n1=int(input("Enter first number:"))
n2=int(input("Enter second number:"))
print("Sum of",n1,"and",n2,"is =", n1+n2)
print("Difference of",n1,"and",n2,"is =", n1-n2)
print("Product of",n1,"and",n2,"is =", n1*n2)
if n2!=0:
    print("Division of",n1,"and",n2,"is =", n1/n2)
    print("Modulus of",n1,"and",n2,"is =", n1%n2)
else:
    print("divisor cannot be 0")
print("Exponent of",n1,"and",n2,"is =", n1**n2)